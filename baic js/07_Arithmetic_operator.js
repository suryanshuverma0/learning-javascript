//Arithmetic operator

let a=50;
let b=5;

console.log("result of a+b is " , a+b);//Addition
console.log("result of a-b is " , a-b); //Subtraction
console.log("result of a*b is " , a*b);//Multiplication
console.log("result of a/b is " , a/b);//Division
console.log("result of a%b is " , a%b);//Modulo ->to find the reminder
console.log("result of a**b is " , a**b);// Exponential ->to find the power 

//Increment and Decrement Operator

let aa=5;
aa++;//post increment operator
console.log(aa);

++aa;//pre increment operator 
console.log(aa);


let bb=5;
bb--;//post increment operator
console.log(bb);

--bb;//pre increment operator 
console.log(bb);

let cc=5;
++cc;//post increment operator
console.log(cc);

cc--;//pre increment operator 
console.log(cc);